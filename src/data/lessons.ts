const gKey = (s: string) => s.split('').map(c => map[c] || c).join('');

// Helper to generate repetitive lesson words
const drill = (wordsList: string[], repeats: number = 3): LessonWord[] => {
  const result: LessonWord[] = [];
  wordsList.forEach(w => {
    for (let i = 0; i < repeats; i++) {
      result.push({ keys: gKey(w), bangla: w });
    }
  });
  return result;
};

const plainPara = (sentences: string[]): LessonWord[] => {
  return sentences.map(s => ({ keys: gKey(s), bangla: s }));
};

